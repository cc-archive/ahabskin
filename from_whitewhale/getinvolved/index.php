			<h2><img src="../images/headers/getinvolved.gif" alt="What is Open Education?" width="317" height="57" /></h2>
		  <p id="intro">You’re already a part of the open education movement just by being here and using open resources.  But if you’d like to take the next step, there are many other ways to get involved, including joining the open education online community here on our site.</p>
		  <div class="three columns">
			<div class="first column">
<p>A lot of OER are hosted by institutions, which tends to serve as an arbiter of quality (e.g., <a href="http://www.ocwconsortium.org/index.php">OCWC</a>). Other OER are contributed to open repositories where they are subject to reviews and ratings from the user community (e.g., <a href="http://cnx.org">Connexions</a>).</p>
			<p>While the origins and forms of OER are varied and scattered today, our expectation is that OER numbers will continue to grow exponentially and that new and existing systems of sorting and evaluating those OER will continue to emerge and be improved. In theory, anyone who has an insight on how to enable educational access and increase understanding should participate in the ongoing creation and use of OER.</p>
